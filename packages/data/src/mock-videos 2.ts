export const getVideoById = (id: string): UserVideo | undefined =>
  mockVideos.find((v) => v.id === id);

export const getVideosByUserId = (userId: string): UserVideo[] =>
  mockVideos.filter((v) => v.userId === userId);

export const getVideosByTrickId = (trickId: string): UserVideo[] =>
  mockVideos.filter((v) => v.trickId === trickId);

export const getApprovedVideos = (): UserVideo[] =>
  mockVideos.filter((v) => v.isApproved);

export const getVideosByTag = (tag: string): UserVideo[] =>
  mockVideos.filter((v) => v.tags.includes(tag));

export const getTopVideos = (limit: number = 10): UserVideo[] =>
  [...mockVideos].filter((v) => v.isApproved).sort((a, b) => b.likes - a.likes).slice(0, limit);

export const getRecentVideos = (limit: number = 10): UserVideo[] =>
  [...mockVideos].filter((v) => v.isApproved).sort((a, b) => b.createdAt.getTime() - a.createdAt.getTime()).slice(0, limit);
